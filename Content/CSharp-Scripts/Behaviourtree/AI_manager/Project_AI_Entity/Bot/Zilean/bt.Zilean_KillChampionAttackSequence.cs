                                          out CastSpellTimeThreshold,
                                          Self,
                                          Self,
                                          2,
                                          1,
                                          PreviousSpellCast,
                                          PreviousSpellCastTarget,
                                          PreviousSpellCastTime,
                                          CastSpellTimeThreshold, default
                                          ,
                                          false)
                              ) ||
                              // Sequence name :UseW
                              (
                                    GetSpellSlotCooldown(
                                          out Cooldown,
                                          Self,
                                          SPELLBOOK_CHAMPION,
                                          0) &&
                                    GreaterFloat(
                                          Cooldown,
