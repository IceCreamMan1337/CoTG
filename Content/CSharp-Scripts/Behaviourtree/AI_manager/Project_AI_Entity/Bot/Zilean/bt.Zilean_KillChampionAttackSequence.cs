                              // Sequence name :CastE
                              (
                                    TestUnitHasBuff(
                                          Target, default
                                          ,
                                          "TimeWarpSlow",
                                          true) &&
                                    GetUnitHealthRatio(
                                          out TargetHealthRatio,
                                          Target) &&
                                    LessFloat(
                                          TargetHealthRatio,
                                          0.6f) &&
                                    canCastChampionAbilityClass.CanCastChampionAbility(
                                          Self,
                                          2,
                                          PreviousSpellCastTime,
                                          CastSpellTimeThreshold,
                                          PreviousSpellCastTarget,
                                          Self,
                                          PreviousSpellCast,
                                          false,
                                          false) &&
